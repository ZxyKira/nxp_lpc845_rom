#include <stdint.h>
#include "fw_chip.h"

/*-----------------------------------------------------------------------------------------
 *    Parameter
 */
 
/*-----------------------------------------------------------------------------------------
 *    Extern Function/Variable
 */
extern void periph_io_startup(void);
extern void timer_init(void);
extern void terminal_init0(void);
/*-----------------------------------------------------------------------------------------
 *    Local Type/Structure
 */

/*-----------------------------------------------------------------------------------------
 *    Local Variable
 */


/*-----------------------------------------------------------------------------------------
 *    Inline Function
 */
 
/*-----------------------------------------------------------------------------------------
 *    Local Function
 */ 
void start_hw(){
	fw_io_api.init(0);
	
	fw_usart_api.init(0);
}

/*-----------------------------------------------------------------------------------------
 *    Public Function
 */ 

void start(){
	fw_chip_init();
	start_hw();
	
	periph_io_startup();
	timer_init();
	terminal_init0();
}

