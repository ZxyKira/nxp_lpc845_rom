fw_io_entity_t LED[8];

/*-----------------------------------------------------------------------------------------
 *    Inline Function
 */

/*-----------------------------------------------------------------------------------------
 *    Local Function
 */
static void periph_io_init(void){
	int i;
	
	for(i=0; i<8; i++){
		
		/* Get io PIO1_i profile to LED[i] */
		LED[i] = fw_io_api.getEntity(0, 1, i);
		fw_io_entity_api.setOutput(LED[i]);

		fw_io_entity_api.setLow(LED[i]);
		/* Set io to low */
	}
}
 
/*-----------------------------------------------------------------------------------------
 *    Public Function
 */
void periph_io_startup(){
	periph_io_init();
}

